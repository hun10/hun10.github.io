export function raw2bin(unsignedBytes) {
    const address = 0o040000
    const length = unsignedBytes.length

    const bin = new Uint8Array(length + 4)
    bin[0] = address & 0xFF
    bin[1] = (address >> 8) & 0xFF
    bin[2] = length & 0xFF
    bin[3] = (length >> 8) & 0xFF

    for (let i = 0; i < length; i++) {
        bin[i + 4] = unsignedBytes[i]
    }

    return bin
}

export function named2bin(unsignedBytes) {
    const address = unsignedBytes[0] | (unsignedBytes[1] << 8)
    const length = unsignedBytes[2] | (unsignedBytes[3] << 8)

    const bin = new Uint8Array(length + 4)
    bin[0] = address & 0xFF
    bin[1] = (address >> 8) & 0xFF
    bin[2] = length & 0xFF
    bin[3] = (length >> 8) & 0xFF

    for (let i = 0; i < length; i++) {
        bin[i + 4] = unsignedBytes[i + 20]
    }

    return bin
}

export function bin2tape(nameUnsignedBytesKoi8, unsignedBytes) {
    const length = unsignedBytes[2] | (unsignedBytes[3] << 8)
    let checksum = 0

    const body = new Uint8Array(length)

    for (let i = 0; i < length; i++) {
        body[i] = unsignedBytes[i + 4]

        checksum += body[i]

        if (checksum > 0xFFFF) {
            checksum++
            checksum &= 0xFFFF
        }
    }

    let tapeHead = 0
    const tape = new Int16Array(
        preamble.length + 20 * 8 * 4 + preBody.length + length * 8 * 4 + 2 * 8 * 4 + ending.length
    )

    function put(sequence) {
        for (let i = 0; i < sequence.length; i++) {
            tape[tapeHead++] = sequence[i]
        }
    }

    function putByte(byte) {
        for (let i = 0; i < 8; i++, byte >>= 1) {
            if (byte & 1) {
                put(one)
            } else {
                put(zero)
            }

            if (i !== 7) {
                put(syncBit)
            }
        }
    }

    function putManyBytes(bytes) {
        for (let i = 0; i < bytes.length; i++) {
            putByte(bytes[i])

            if (i !== bytes.length - 1) {
                put(syncByte)
            }
        }
    }

    put(preamble)

    const header = new Uint8Array(20)
    header[0] = unsignedBytes[0]
    header[1] = unsignedBytes[1]
    header[2] = unsignedBytes[2]
    header[3] = unsignedBytes[3]
    for (let i = 0; i < 16; i++) {
        header[i + 4] = nameUnsignedBytesKoi8[i]
    }
    putManyBytes(header)
    put(syncHeader)

    put(preBody)
    putManyBytes(body)
    put(syncBody)

    putManyBytes([ checksum & 0xFF, checksum >> 8 ])
    put(syncChecksum)

    put(ending)

    return tape.map(v => Math.round(v / 2))
}

